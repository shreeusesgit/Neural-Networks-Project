import pandas as pd
import math
from random import seed, random, randrange
from sklearn.preprocessing import MinMaxScaler
from sklearn.metrics import confusion_matrix
import seaborn as sns
import matplotlib.pyplot as plt

df = pd.read_csv("Sonar.csv", header=None)
df = df.drop_duplicates().reset_index(drop=True)

X = df.iloc[:, :-1].values
y = df.iloc[:, -1].map({'R': 0, 'M': 1}).values

scaler = MinMaxScaler()
X = scaler.fit_transform(X)

dataset = [list(X[i]) + [y[i]] for i in range(len(y))]

def initialize_network(n_inputs, n_hidden, n_outputs):
    network = []
    hidden_layer = [{'weights':[random()*0.5 for _ in range(n_inputs + 1)]}
                    for _ in range(n_hidden)]
    network.append(hidden_layer)
    output_layer = [{'weights':[random()*0.5 for _ in range(n_hidden + 1)]}
                    for _ in range(n_outputs)]
    network.append(output_layer)
    return network

def activate(weights, inputs):
    activation = weights[-1]
    for i in range(len(weights)-1):
        activation += weights[i] * inputs[i]
    return activation

def transfer(x):
    return 1.0 / (1.0 + math.exp(-x))

def forward_propagate(network, row):
    inputs = row
    for layer in network:
        new_inputs = []
        for neuron in layer:
            neuron['output'] = transfer(activate(neuron['weights'], inputs))
            new_inputs.append(neuron['output'])
        inputs = new_inputs
    return inputs

def backward_propagate_error(network, expected):
    for i in reversed(range(len(network))):
        layer = network[i]
        errors = []
        if i == len(network) - 1:
            for j, neuron in enumerate(layer):
                errors.append(expected[j] - neuron['output'])
        else:
            for j in range(len(layer)):
                error = 0.0
                for neuron in network[i + 1]:
                    error += neuron['weights'][j] * neuron['delta']
                errors.append(error)
        for j, neuron in enumerate(layer):
            neuron['delta'] = errors[j] * neuron['output'] * (1 - neuron['output'])

def update_weights(network, row, l_rate):
    for i, layer in enumerate(network):
        inputs = row if i == 0 else [n['output'] for n in network[i - 1]]
        for neuron in layer:
            for j in range(len(inputs)):
                neuron['weights'][j] += l_rate * neuron['delta'] * inputs[j]
            neuron['weights'][-1] += l_rate * neuron['delta']

def stratified_folds(dataset, n_folds):
    class0 = [row for row in dataset if row[-1] == 0]
    class1 = [row for row in dataset if row[-1] == 1]
    folds = [[] for _ in range(n_folds)]
    for i, row in enumerate(class0):
        folds[i % n_folds].append(row)
    for i, row in enumerate(class1):
        folds[i % n_folds].append(row)
    return folds

def train_network(dataset, l_rate, n_epoch, n_outputs, n_hidden, n_folds):
    folds = stratified_folds(dataset, n_folds)
    scores, all_actual, all_predicted = [], [], []

    for idx, fold in enumerate(folds):
        train_set = [row for f in folds if f != fold for row in f]
        test_set = fold
        network = initialize_network(len(dataset[0])-1, n_hidden, n_outputs)

        for _ in range(n_epoch):
            for row in train_set:
                outputs = forward_propagate(network, row[:-1])
                expected = [0]*n_outputs
                expected[row[-1]] = 1
                backward_propagate_error(network, expected)
                update_weights(network, row[:-1], l_rate)

        correct = 0
        for row in test_set:
            outputs = forward_propagate(network, row[:-1])
            pred = outputs.index(max(outputs))
            all_actual.append(row[-1])
            all_predicted.append(pred)
            if pred == row[-1]:
                correct += 1

        acc = correct / len(test_set) * 100
        scores.append(acc)
        print(f"Fold {idx+1} Accuracy: {acc:.2f}%")

    print(f"\nMean Accuracy: {sum(scores)/len(scores):.2f}%")

    cm = confusion_matrix(all_actual, all_predicted)
    sns.heatmap(cm, annot=True, fmt='g')
    plt.xlabel("Predicted")
    plt.ylabel("Actual")
    plt.title("Confusion Matrix")
    plt.show()

seed(1)
train_network(
    dataset=dataset,
    l_rate=0.1,
    n_epoch=600,
    n_outputs=2,
    n_hidden=15,
    n_folds=10
)



