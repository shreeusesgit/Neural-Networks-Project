from random import seed, random, randrange
from math import exp
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.metrics import confusion_matrix
from sklearn.preprocessing import MinMaxScaler
from sklearn.datasets import load_breast_cancer

data = load_breast_cancer()
X = data.data
y = data.target

scaler = MinMaxScaler()
X_scaled = scaler.fit_transform(X)
dataset = [list(X_scaled[i]) + [y[i]] for i in range(len(X_scaled))]

def initialize_networks(n_inputs, n_hiddens, n_outputs):
  network = []
  hidden_layers = [{'weights':[random() for i in range(n_inputs + 1)]} for i in range(n_hiddens)]
  network.append(hidden_layers)
  output_layer = [{'weights': [random() for i in range(n_hiddens + 1)]}for i in range(n_outputs)]
  network.append(output_layer)
  return network

def activate(weights, inputs):
    activation = weights[-1]   # bias
    for i in range(len(inputs)):
        activation += weights[i] * inputs[i]
    return activation

def transfer(activation):
  return 1.0 / (1.0 + exp(-activation))

def forward_propagate(network, row):
    inputs = row
    for layer in network:
        new_inputs = []
        for neuron in layer:
            activation = activate(neuron['weights'], inputs)
            neuron['output'] = transfer(activation)
            new_inputs.append(neuron['output'])
        inputs = new_inputs
    return inputs

def backward_propagate_error(network, expected):
    for i in reversed(range(len(network))):
        layer = network[i]
        errors = []
        if i != len(network) - 1:
            for j in range(len(layer)):
                error = 0.0
                for neuron in network[i + 1]:
                    error += neuron['weights'][j] * neuron['delta']
                errors.append(error)
        else:
            for j in range(len(layer)):
                neuron = layer[j]
                errors.append(expected[j] - neuron['output'])
        for j in range(len(layer)):
            neuron = layer[j]
            neuron['delta'] = errors[j] * neuron['output'] * (1 - neuron['output'])

def update_weights(network, row, l_rate):
    for i in range(len(network)):
        inputs = row
        if i != 0:
            inputs = [neuron['output'] for neuron in network[i - 1]]

        for neuron in network[i]:
            for j in range(len(inputs)):
                neuron['weights'][j] += l_rate * neuron['delta'] * inputs[j]
            neuron['weights'][-1] += l_rate * neuron['delta']

def train_network(dataset, l_rate, n_epoch, n_outputs, n_hidden, n_folds):
    dataset_copy = list(dataset)
    fold_size = int(len(dataset) / n_folds)
    folds = []

    for _ in range(n_folds):
        fold = []
        while len(fold) < fold_size:
            index = randrange(len(dataset_copy))
            fold.append(dataset_copy.pop(index))
        folds.append(fold)

    scores = []

    for fold_idx, fold in enumerate(folds):
        train_set = [row for f in folds if f != fold for row in f]
        test_set = [list(row) for row in fold]

        network = initialize_networks(len(dataset[0])-1, n_hidden, n_outputs)

        for _ in range(n_epoch):
            for row in train_set:
                outputs = forward_propagate(network, row[:-1])
                expected = [0 for _ in range(n_outputs)]
                expected[row[-1]] = 1
                backward_propagate_error(network, expected)
                update_weights(network, row[:-1], l_rate)

        predicted, actual = [], []
        for row in test_set:
            outputs = forward_propagate(network, row[:-1])
            predicted.append(outputs.index(max(outputs)))
            actual.append(row[-1])

        accuracy = sum(1 for i in range(len(actual)) if actual[i] == predicted[i]) / len(actual) * 100
        scores.append(accuracy)
        print(f'Fold {fold_idx+1} Accuracy: {accuracy:.2f}%')

    print(f'\nMean Accuracy: {sum(scores)/len(scores):.2f}%')

    cm = confusion_matrix(actual, predicted)
    sns.heatmap(cm, annot=True, fmt='g')
    plt.xlabel('Predicted')
    plt.ylabel('Actual')
    plt.title('Breast Cancer – Confusion Matrix')
    plt.show()

seed(1)
train_network(
    dataset=dataset,
    l_rate=0.3,
    n_epoch=5,
    n_outputs=2,
    n_hidden=10,
    n_folds=10
)