import numpy as np
import matplotlib.pyplot as plt

class myGaussianFuzzySet:
  def __init__(self, c, sigma):
    self.c = c
    self.sigma = sigma

  def membership(self, x):
    return np.exp(-((x -self.c)**2) / (2*self.sigma**2))

  def area(self):
    return self.sigma * np.sqrt(2 * np.pi)

